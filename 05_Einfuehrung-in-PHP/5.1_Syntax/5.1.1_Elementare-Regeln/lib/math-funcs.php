<?php

/**
 * @param int $a
 * @param int $b
 * @return int
 */
function add(int $a, int $b) : int
{
    return $a + $b;
}

/**
 * @param int $a
 * @param int $b
 * @return int
 */
function sub(int $a, int $b) : int
{
    return $a - $b;
}

/**
 * @param int $a
 * @param int $b
 * @return int
 */
function mul(int $a, int $b) : int
{
    return $a * $b;
}

/**
 * @param int $a
 * @param int $b
 * @return float
 */
function div(int $a, int $b) : float
{
    return $a / $b;
}