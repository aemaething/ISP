<?php

$a = 12;
$b = 3;

// Addition
$c = $a + $b;
echo $c, "\n";
// 15

// Subtraktion
$c = $a - $b;
echo $c, "\n";
// 9

// Multiplikation
$c = $a * $b;
echo $c, "\n";
// 36

// Division
$c = $a / $b;
echo $c, "\n";
// 4

// Modulo
$c = $a % $b;
echo $c, "\n";
// 0

// Potenzieren
$c = $a ** $b;
echo $c, "\n";
// 1728